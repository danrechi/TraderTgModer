    doc.add_paragraph('• Пользовательский интерфейс: По команде /news пользователям выдается inline-клавиатура с выбором категорий новостей. При выборе новости бот обращается к API OpenAI (модель gpt-4o-mini) для генерации краткого 2-3 строчного смысла статьи (суммаризация), избавляя от необходимости читать простыню текста.', style='List Bullet')
    doc.add_paragraph('• Модерация чатов: Бот перехватывает все сообщения в привязанных группах, проверяет их по паттернам из БД (регулярные выражения, ключевые слова) и может автоматически удалять спам, выдавать предупреждения или блокировать нарушителей.', style='List Bullet')


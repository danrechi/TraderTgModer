    p = doc.add_paragraph('Проект представляет собой комплексную автоматизированную систему (Telegram-бот + Веб-админка) для агрегации, интеллектуальной классификации, суммаризации и публикации финансовых новостей. Основная цель системы — снижение информационного шума в трейдерских чатах и предоставление краткой, строго категоризированной информации из проверенных RSS-источников, а также автоматическая модерация групповых чатов.')

    # Архитектура
    doc.add_heading('2. Архитектура и стек технологий', level=1)
    
    doc.add_heading('2.1. Backend (FastAPI, Python)', level=2)
    doc.add_paragraph('REST API разработано на фреймворке FastAPI. Для работы с базой данных используется ORM SQLAlchemy. В качестве хранилища используется легковесная СУБД SQLite (база trader_news.db). Реализована система JWT-авторизации для защиты эндпоинтов административной панели.')

    doc.add_heading('2.2. Frontend (React, Vite)', level=2)
    doc.add_paragraph('Веб-админка написана на React + Vite. Дизайн выполнен на чистом CSS в современном темном стиле (Dark Theme с неоновыми акцентами). Используется React Router для маршрутизации (Dashboard, Sources, News, Rules, Login) и Context API для глобального управления состоянием авторизации.')

